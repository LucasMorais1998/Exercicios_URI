using System;

namespace _1096.Sequencia_IJ_2
{
    class Program
    {
        static void Main(string[] args)
        {

            for (int i = 1; i < 10; i+=2)
            {
                for (int b = 7; b > 4; b--)
                {
                    Console.WriteLine("I=" + i + " " + "J=" + b);
                }
            }
        }
    }
    
