using System;
using System.Globalization;

namespace _1759.Ho_Ho_Ho
{
    class Program
    {
        static void Main(string[] args)
        {
            int N = int.Parse(Console.ReadLine());

            for (int i = 0; i < N - 1; i++)
            {
                Console.Write("Ho ");
            }

            Console.Write("Ho!\n");
        }
    }
}
